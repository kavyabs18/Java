package com.kodnest.String;

class compareTo {
public static void main(String[] args) {
	String s1 = "Apple";
	String s2 = "Apple";
	System.out.println(s1.compareTo(s2));
	
	String s3 = "Apple";
	String s4 = "Bpple";
	System.out.println(s3.compareTo(s4));
	
	String s5 = "Bpple";
	String s6 = "Apple";
	System.out.println(s5.compareTo(s6));
}
}
